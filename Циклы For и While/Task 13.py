# Дано натуральное число. Определить номер цифры 3 в нем, считая от конца числа. 
# Если такой цифры нет, ответом должно быть число 0, если таких цифр в числе несколько — 
# должен быть определен номер самой правой из них.

def find_digit_position(n):
    # Преобразовать число в строку для легкого доступа к цифрам
    num_str = str(n)
    
    # Инициализировать позицию цифры 3
    position = 0
    
    # Итерировать по цифрам с конца числа
    for i in range(len(num_str) - 1, -1, -1):
        if num_str[i] == '3':
            # Если найдена цифра 3, определить ее позицию с конца
            position = len(num_str) - i
            break
    
    return position

# Ввод натурального числа
n = int(input("Введите натуральное число: "))

# Вывод позиции цифры 3
print("Позиция цифры 3 с конца числа:", find_digit_position(n))
