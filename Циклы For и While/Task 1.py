# Написать программу, 
# которая будет показывать на экран квадрат числа, 
# введенного пользователем. Пользователь должен сам решать – выйти из 
# программы или продолжить ввод. 
# (Подсказка – необходимо запустить бесконечный цикл, в котором предусмотреть его прерывание, 
# при наступлении определённого условия).

def main():
    while True:
        user_input = input("Введите число или 'q', чтобы выйти: ")
        
        if user_input.lower() == 'q':
            print("Программа завершена.")
            break
        
        try:
            number = float(user_input)
            square = number ** 2
            print(f"Квадрат числа {number} равен {square}.")
        except ValueError:
            print("Некорректный ввод. Пожалуйста, введите число или 'q' для выхода.")

if __name__ == "__main__":
    main()

